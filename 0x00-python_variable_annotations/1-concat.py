#!/usr/bin/env python3
"""
1. Basic annotations - concat
"""


def concat(str1: str, str2: str) -> str:
    """
     Concat two strings together.

     Args:
         str1 (str): The first string to concat.
         str2 (str): The second string to concat.

    Return:
        str: The concat of two strings.
    """
    return str1 + str2
