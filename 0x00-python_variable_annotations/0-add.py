#!/usr/bin/env python3
"""
0. Basic annotations - add
"""


def add(a: float, b: float) -> float:
    """
    Add two floating-point numbers.

    Args:
        a (float): The first number to add.
        b (float): The second number to add.

    Return:
        float: The sum of the two input numbers
    """
    return a + b
